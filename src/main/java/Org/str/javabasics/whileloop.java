package Org.str.javabasics;

public class whileloop {
    public static void main(String[] args) {

        int j=0;
        while(j<5){
            System.out.println(j); // 0,1,2,3,4
            j++;
        }
        int i=0;
        while(i<5){
            System.out.println(i);// it will print continuously bcz no condition here
        }
        while(true){
            System.out.println("Hello World");// it will print continuously
        }
    }
}
