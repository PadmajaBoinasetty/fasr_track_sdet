package Org.str.javabasics;

public class increment_decrement_oprerators {
    public static void main(String[] args) {
        int j=0;
        System.out.println(j++);//0
        System.out.println(j);//1

        int k=0;
        System.out.println(++k);//1
        System.out.println(k);//1
    }
}
